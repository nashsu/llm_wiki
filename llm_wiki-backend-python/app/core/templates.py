"""Project template definitions and built-in templates."""

from pydantic import BaseModel


class ProjectTemplate(BaseModel):
    """A project template defining default directory structure and content."""

    id: str
    name: str
    icon: str
    description: str
    extra_dirs: list[str]
    schema_content: str
    purpose_content: str


# ---------------------------------------------------------------------------
# Registry
# ---------------------------------------------------------------------------

_templates: dict[str, ProjectTemplate] = {}


def _register(template: ProjectTemplate) -> None:
    _templates[template.id] = template


